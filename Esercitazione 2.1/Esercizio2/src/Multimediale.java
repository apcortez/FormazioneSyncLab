abstract class Multimediale {
    protected String titolo;
    protected String tipo;


    public Multimediale(String titolo, String tipo){
        this.titolo = titolo;
        this.tipo = tipo;
    }



    public String getTitolo() {
        return titolo;
    }

    public void setTitolo(String titolo) {
        this.titolo = titolo;
    }


}

